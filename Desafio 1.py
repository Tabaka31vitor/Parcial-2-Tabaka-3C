#SOMA DE NUMEROS
#criação das variáveis n1 e n2 para fazer a escolha dos numeros que serão somados
n1 = float(input("escolha um numero"))
n2 = float(input('escola outro numero'))
#a variável 'soma' foi criada para somar as duas variáveis iniciais, no caso 'n1' e 'n2'
soma = n1 + n2
#o código 'print' serve para fazer a impressão de algo, nesse caso sera colocado o resultado da soma de 'n1' + 'n2'
print(soma)
