#AREA DO TRIANGULO
#'base' e 'altura' foram as variaveis criadas para fazer as perguntas de base e altura do triangulo
base = float(input("qual a base do triangulo?"))
altura = float(input("qual a altura do triangulo?"))

#com base nas respostas das variaveis acima foi colocada a fórmula da area do triangulo para ser resolvido segundo a resposta da base e altura
area = (base * altura) / 2
#'print' é para mostrar a resposta do calculo da formula do triangulo
print(area)
